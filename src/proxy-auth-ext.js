// Generates an unpacked MV3 extension that supplies proxy credentials via
// chrome.webRequest.onAuthRequired. This is the standard way to make Chromium
// authenticate to a proxy, because --proxy-server=user:pass@host:port is ignored by Chrome.
const fs = require('fs');
const path = require('path');

// Creates (or refreshes) the extension dir for a profile and returns its path.
function createProxyAuthExtension(baseDir, profileId, proxy) {
  const dir = path.join(baseDir, 'proxy-auth', String(profileId));
  fs.mkdirSync(dir, { recursive: true });

  const manifest = {
    name: 'Aether Proxy Auth',
    version: '1.0.0',
    manifest_version: 3,
    permissions: ['proxy', 'webRequest', 'webRequestAuthProvider'],
    host_permissions: ['<all_urls>'],
    background: { service_worker: 'bg.js' }
  };
  fs.writeFileSync(path.join(dir, 'manifest.json'), JSON.stringify(manifest, null, 2));

  const bg = `// Auto-generated by Aether — supplies proxy credentials on demand.
const CREDENTIALS = ${JSON.stringify({ username: proxy.username, password: proxy.password })};
let inflight = {};
chrome.webRequest.onAuthRequired.addListener(
  (details, callback) => {
    if (details.isProxy) {
      // Avoid auth loops: only answer once per requestId
      if (inflight[details.requestId]) { callback({}); return; }
      inflight[details.requestId] = true;
      callback({ authCredentials: CREDENTIALS });
    } else {
      callback({});
    }
  },
  { urls: ['<all_urls>'] },
  ['asyncBlocking']
);
chrome.webRequest.onCompleted.addListener(d => { delete inflight[d.requestId]; }, { urls: ['<all_urls>'] });
chrome.webRequest.onErrorOccurred.addListener(d => { delete inflight[d.requestId]; }, { urls: ['<all_urls>'] });
`;
  fs.writeFileSync(path.join(dir, 'bg.js'), bg);
  return dir;
}

module.exports = { createProxyAuthExtension };
